package exerciciosLista11;
import java.util.Scanner;
public class Ex08 {
	 public static void main(String[] args) {
	        System.out.println("Quadrados dos números de 1 a 20:");

	        for (int i = 1; i <= 20; i++) {
	            int quadrado = i * i;
	            System.out.println(i + "² = " + quadrado);
	        }
	    }
}
